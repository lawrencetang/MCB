/*******************************************************************************
* Function Name  : RTC_IRQHandler
* Description    : This function handles RTC global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void RTC_IRQHandler(void)
{		
	/*
	 * RTC_ITConfig(RTC_IT_SEC, DISABLE)，不让RTC进入秒中断
	 * 即此中断不会进入，实际应用中也不需要进入此中断
	 * 减少中断进入次数
	 */		
	OS_CPU_SR cpu_sr;	 

    OS_ENTER_CRITICAL(); 
    OSIntNesting++;
  	OS_EXIT_CRITICAL(); 
	if (RTC_GetITStatus(RTC_IT_SEC) != RESET)
	{
	    RTC_ClearITPendingBit(RTC_IT_SEC);
	    RTC_WaitForLastTask();
	}
		 
    OS_ENTER_CRITICAL();
	if (OSIntNesting > 0)
	{ 
    	OSIntNesting--;
	}
  	OS_EXIT_CRITICAL(); 
}

